# ========第一天：变量与数据类型 ==========

# 1.print 输出
print("hello python")
print("小小茄的一第一天")

# 2.变量
name = "小茄"
age = 28
sex = "男"
job = "软件测试工程师"
goal = "12个月精通python"
is_student = True

# 3. f-string 格式化输出
print(f"我的名字是{name}")
print(f"我的年龄是{age}")
print(f"我的性别是{sex}")
print(f"我的工作是{job}")
print(f"我的目标是{goal}")

# 4.数据类型查看
print(type(name))
print(type(age))
print(type(sex))
print(type(job))
print(type(goal))
print(type(is_student))

# 5.数据类型转换
score = "100"
print(type(score))
print(int(score))

# 6.简单运算
print(10 + 20)
print(10 - 20)
print(10 * 20)
print(10 / 3)
print(10 // 3)
print(10 % 3)
print(10 ** 3)

# 7.第一天打卡完成
print("=" * 30)
print("第一天打卡完成!")
